package data_type;

public class Static_variable {
	static boolean x;
	static int y=4;
	static byte a;
	static short b;
	static char c;
	static String d;
	static long e;
	public static void main(String[] args) {
		
		System.out.println(x+"+hai");
		System.out.println(y+"+hai");
		System.out.println(a+"+hai");
		System.out.println(b+"+hai");
		System.out.println(c);
		System.out.println(d+"+hai");
		System.out.println(e+"+hai");
		
		
	}

}
